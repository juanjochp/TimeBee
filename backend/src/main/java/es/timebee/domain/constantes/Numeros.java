package es.timebee.domain.constantes;

/**
 * {@code Numeros} es una clase utilitaria que agrupa constantes numéricas
 * específicas usadas en la aplicación TimeBee.
 * <p>
 * Aunque pueda parecer una lista simple de números, centralizar estos valores
 * ayuda a evitar “números mágicos” repartidos por el código, facilitando así
 * el mantenimiento y la comprensión del proyecto.
 * <p>
 * Desde tamaños máximos hasta valores de referencia, aquí están todos reunidos.
 */
public class Numeros {
    public static final int NUEVE = 9;
    public static final int DOCE = 12;
    public static final int CATORCE = 14;
    public static final int DIECISIETE = 17;
    public static final int VEINTICUATRO = 24 ;
    public static final int DOSCIENTOSCINCUENTAYCINCO = 255;
}
